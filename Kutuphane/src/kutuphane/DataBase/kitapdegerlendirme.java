    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        kitapDegerlendir kitapdegerlendir = new kitapDegerlendir();
        KitapManager kitapmanager = new KitapManager();
        KullanıcıManager kullanıcıManager = new KullanıcıManager();
        String loggedInUsername = kullanıcıManager.getLoggedInUsername();
        kullanıcıadı.setText(loggedInUsername);
        String kitapAdi = kitapadi.getText().toUpperCase();
        String secilenPuan = (String) puan.getSelectedItem();
 
        

    if (kitapmanager.bookControl(kitapAdi) == 0){
    kitapdegerlendir.insertDegerlendirme(kitapadi.getText().toUpperCase(), degerlendirme.getText().toUpperCase(), secilenPuan, loggedInUsername);

    JOptionPane.showMessageDialog(null, "KİTAP EKLENDİ!");
    kitapadi.setText("");
    degerlendirme.setText("");
    puan.setSelectedIndex(0);
    kullanıcıadı.setText(loggedInUsername);
    loadKitapListToTable();
    }else{
        JOptionPane.showMessageDialog(null, "HATA: Kitap bulunamadı. Lütfen geçerli bir kitap adı girin.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void loadKitapListToTable() {
        try {
            // Kitap listesini al
            DefaultListModel<Degerlendir> kitapListModel = kitapdegerlendir.getDegerlendirmeBooks();


            // DefaultTableModel oluştur
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.addColumn("Kitap Adı");
            tableModel.addColumn("Değerlendirme");
            tableModel.addColumn("Puan");
            tableModel.addColumn("Kullanıcı Adı");
            

            // Kitap listesini DefaultTableModel'e ekle
            for (int i = 0; i < kitapListModel.getSize(); i++) {
                Degerlendir degerlendir = kitapListModel.get(i);
                Object[] row = {degerlendir.getKitap_adi(), degerlendir.getDegerlendirme(), degerlendir.getPuan(), degerlendir.getKullanici_adi()};
                tableModel.addRow(row);
            }

            // JTable'a DefaultTableModel'i ekle
            kitaplardegerlendirmeler.setModel(tableModel);
        } catch (SQLException e) {
            e.printStackTrace();
            // Hata durumunda gerekli işlemler yapılabilir
        }
    }
